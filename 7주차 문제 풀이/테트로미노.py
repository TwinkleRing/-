import sys
input = sys.stdin.readline

n ,m = map(int,input().split())
array = [list(map(int,input().split())) for _ in range(n)]

tetrominos = [
    [(0,0), (0,1), (1,0), (1,1)], # ㅁ
    [(0,0), (0,1), (0,2), (0,3)], # ㅡ
    [(0,0), (1,0), (2,0), (3,0)], # ㅣ
    [(0,0), (0,1), (0,2), (1,0)], 
    [(1,0), (1,1), (1,2), (0,2)],
    [(0,0), (1,0), (1,1), (1,2)], # ㄴ
    [(0,0), (0,1), (0,2), (1,2)], # ㄱ
    [(0,0), (1,0), (2,0), (2,1)],
    [(2,0), (2,1), (1,1), (0,1)],
    [(0,0), (0,1), (1,0), (2,0)], 
    [(0,0), (0,1), (1,1), (2,1)],
    [(0,0), (0,1), (0,2), (1,1)], # ㅜ
    [(1,0), (1,1), (1,2), (0,1)], # ㅗ
    [(0,0), (1,0), (2,0), (1,1)], # ㅏ
    [(1,0), (0,1), (1,1), (2,1)], # ㅓ
    [(1,0), (2,0), (0,1), (1,1)],
    [(0,0), (1,0), (1,1), (2,1)],
    [(1,0), (0,1), (1,1), (0,2)],
    [(0,0), (0,1), (1,1), (1,2)]
]

def count(x, y, t) :
    value = 0
    global n, m
    for dx, dy in t :
        nx = x + dx
        ny = y + dy
        if 0 <= nx < n and 0 <= ny < m :
            value += array[nx][ny]
        else :
            return 0

    return value 

ans = 0
for i in range(n) :
    for j in range(m) :
        for tetromino in tetrominos :
            ans = max(ans, count(i, j, tetromino))

print(ans)